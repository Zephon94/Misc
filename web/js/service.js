$(window).scroll(function(){
    if ($(window).scrollTop() >= 200000)
    {
        $("#floatbar").css({position:'fixed',left:'0',top:'0'});
    }
    else
    {
        $("#floatbar").css({position:'absolute',left:'0',top:'400px'});
    }
});
